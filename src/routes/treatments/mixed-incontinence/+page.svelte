  import { onMount } from "svelte";
  import { gsap } from "gsap";
  import { ScrollTrigger } from "gsap/ScrollTrigger";
  import img from "$lib/assets/services/7.png"

  gsap.registerPlugin(ScrollTrigger);

  let title: string = "Mixed Incontinence";
  let intro: string = ` Relief from Leakage. Confidence Restored.`;
  let body: string = `
If you’re tired of worrying about unexpected leaks—whether it’s a sudden urge or a laugh that turns embarrassing—you’re not alone. Mixed incontinence, also known as combination stress and urge incontinence, is incredibly common. It happens when your bladder muscles and pelvic floor don’t quite sync up, causing both types of leakage. But here’s the good news: real, lasting relief is absolutely possible.

At Dr Corinne Sugihara’s clinic, we specialize in mixed urinary incontinence treatment designed around your unique body and your daily life—not just your symptoms. Here, you’ll never be rushed, dismissed, or told to “just live with it.” We listen first, because understanding your story is the first step toward healing.

Our mixed incontinence specialist clinic offers a range of evidence-based and personalized options—from pelvic floor therapies and bladder retraining to advanced treatments that target both the stress and urge components of leakage. Every plan is built to help you regain comfort, confidence, and control.

You deserve to laugh, exercise, travel, and sleep through the night without fear of accidents. With the right mixed bladder leakage solutions, you can move freely again—without the constant worry or frustration that comes with mixed incontinence.
`;

  let cta: string = " Schedule a consultation today and discover compassionate, personalized care that puts your comfort—and your confidence—first.";

  onMount(() => {
    // Animate background orbs
    gsap.to(".bg-shape", {
      y: 40,
      duration: 6,
      repeat: -1,
      yoyo: true,
      ease: "sine.inOut",
      stagger: { amount: 2 },
    });

    // Gradient text reveal
    gsap.fromTo(
      ".gradient-text",
      { clipPath: "inset(0 100% 0 0)" },
      {
        clipPath: "inset(0 0% 0 0)",
        duration: 1.4,
        ease: "power3.out",
        scrollTrigger: {
          trigger: ".gradient-text",
          start: "top 85%",
        },
      }
    );

    // Fade in sections
    gsap.from(".fade-in", {
      opacity: 0,
      y: 30,
      duration: 1,
      stagger: 0.2,
      ease: "power2.out",
      scrollTrigger: {
        trigger: ".fade-in",
        start: "top 85%",
      },
    });

    // Button hover glow
    document.querySelectorAll(".cta-button").forEach((btn) => {
      btn.addEventListener("mouseenter", () => {
        gsap.to(btn, {
          boxShadow: "0 0 30px 8px rgba(186,104,200,0.45)",
          duration: 0.3,
        });
      });
      btn.addEventListener("mouseleave", () => {
        gsap.to(btn, {
          boxShadow: "0 0 0px 0px rgba(186,104,200,0)",
          duration: 0.3,
        });
      });
    });
  });
</script>

